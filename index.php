					<h2 class="history">OUR HISTORY</h2>
					<p class="first_stanza">Sta.  Monica  is  the  official  name  of  the  barangay.  It  is  situated  in  the  eastern  part  of Concepcion. Rich in its natural resources, especially its vast agricultural lands, it is noted for its fishing grounds and  its  native industries of mats and hats. Culatingan  was the first name of the barrio when it was still located along the bank of the Chico River crossing the Culatingan  Creek.  Its  transfer  to  its  present  place  was  due  to  the  fear  of  the  Spaniards  of the  people  collaborating  with  the  Katipuneros.  That  was  about  the  time  when  General Mariano Llanera was organizing his revolutionary army in Nueva Ecija.</p>
					<p class="sec_stanza">A  most  interesting  name  of  the  Barangay,  and  which  is  still  being referred to is ‘Buntuc Babi’ (head of a boar). It was said that the place was  headed  by  a  Negrito  chieftain,  a  certain  Capitan  Lucindo.  Since the  area  used  to  have  wild  boars,  hunting  was  prevalent  then.  Each hunting  party  though  was  obligated  to  apportion  something  for  the chieftain, usually the pig’s head and was placed frequently among the house  posts.  Passers-by cannot help but exclaim ‘Buntuc Babi’ for such gory sights.</p>
					<p class="third_stanza">In 1950s, when the HDP was prepared, Sta. Monica was composed of   the   following   sitios:   Matalusad,   Magunting,   Tabun,   Libutad, Sampaloc,  Matico,  Mangga  and  Culatingan.  A  number  of  them  have attained  their  independent  barangay  status  hence,  including  Sto. Rosario(Matalusad and Magunting) andCulatingan.</p>
					<p class="forth_stanza">The  barangay  was  believed  to  have  been  established  in  1895.  The  original  settlers included the Panganiban, Ponce, and Rodriguez. As mentioned, the first settlement was in the Culatingan Area, before transferring to the present site. Among theearly tenientes de barrio and cabezas de barangays included Juan Ponce, Manuel Panganiban, Angelo Pineda, Meliton  Dimatulac,  Marciano  Ponce,  Felipe  Arceo,  Basilio  Matic,  Juan  Matic.  Basilio Simon,  Olimpio  Mariano,  Eusebio  Ponce,  Saturnino  Paras,  Crispin  Mendiola,  Alejandro Mallari,  Dalmacio  Caparas,  Delfin  
						<p class="fifth_stanza">It  was  in  1915  that  the  official  name  of  Sta.  Monica  was  given  after  its  patron  saint, replacing that of ‘Buntuc Babi’.</p>
